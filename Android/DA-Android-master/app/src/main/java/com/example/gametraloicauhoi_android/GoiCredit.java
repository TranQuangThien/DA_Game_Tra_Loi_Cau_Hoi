package com.example.gametraloicauhoi_android;

public class GoiCredit {
    private int id;
    private String tenCredit;
    private int credit;
    private int soTien;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTenCredit() {
        return tenCredit;
    }

    public void setTenCredit(String tenCredit) {
        this.tenCredit = tenCredit;
    }

    public int getCredit() {
        return credit;
    }

    public void setCredit(int credit) {
        this.credit = credit;
    }

    public int getSoTien() {
        return soTien;
    }

    public void setSoTien(int soTien) {
        this.soTien = soTien;
    }




}
